    // MARK: - èŽ·å–view çš„æŽ§åˆ¶å™¨
    public func viewController() -> UIViewController? {

        var next = self.next
        while next != nil {
            if (next?.isKind(of: UIViewController.superclass()!))! {
                return next as? UIViewController
            }
            next = next?.next
        }
        return nil
    }

